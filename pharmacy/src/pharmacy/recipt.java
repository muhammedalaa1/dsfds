    private void addbill_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addbill_buttonActionPerformed
        ResultSet rs;
        Double quant = Double.parseDouble(quantity_text.getText().toString());
        Total_price += quant * price;
        int drg_id = 0;
        String Exp_name = "";
        Double drg_price = 0.0;
        int qnt = 0;
        String cmp_name = "";

        try {
            // TODO add your handling code here:
            String txt = medicine_text.getText().toString();
            PreparedStatement stmt = con.prepareStatement("select Drugs_ID , Drugs_Quantity from drugs where Drugs_name like '" + txt + "'");
            rs = stmt.executeQuery();
            while (rs.next()) {
                i = rs.getInt(1);
                quantity = rs.getInt(2);
            }
            if (quant > 0) {
                PreparedStatement stmt453 = con.prepareStatement("INSERT INTO `project`.`sales` (`Drgs_ID` , `Purchased_quantity` ) VALUES (?,?);");

                stmt453.setInt(1, i);
                stmt453.setDouble(2, quant);
                stmt453.executeUpdate();
            }
            if (quantity == 0) {
                JOptionPane.showMessageDialog(this, "No Available Medicenes");
                PreparedStatement stmt1 = con.prepareStatement("select * from drugs;");
                ResultSet rs1;
                rs1 = stmt1.executeQuery();

                while (rs1.next()) {
                    drg_id = rs1.getInt(1);
                    Exp_name = rs1.getString(2);
                    drg_price = rs1.getDouble(3);
                    qnt = rs1.getInt(4);
                    cmp_name = rs1.getString(7);
                }

                PreparedStatement stmt3 = con.prepareStatement("INSERT INTO `project`.`out_of_stock` (`Drug_Id`, `Drugs_NameExp`, `Drug_price` , `Drug_Quantity` , `Company_Name`) VALUES (?, ?, ?,?,?);");

                stmt3.setInt(1, drg_id);
                stmt3.setString(2, Exp_name);
                stmt3.setDouble(3, drg_price);
                stmt3.setInt(4, qnt);

                stmt3.setString(5, cmp_name);

                stmt3.executeUpdate();
            } else {
                if (quantity_text.getText().isEmpty() || medicine_text.getText().isEmpty()) {
                    JOptionPane.showMessageDialog(null, "please put values ");
                } else {
                    if (flag == false) {
                        bill_list_text.setText(bill_list_text.getText() + "               ****************** pharmacy ****************   \n" + "                         Customer Name :      " + billid_text.getText().toString().toUpperCase() + "\n"
                                + "\t ID     MEDICINE      QUANTITY      PRICE           TOTAL \n "
                                + "                           " + i + "       " + medicine_text.getText() + "          " + quantity_text.getText() + "            " + price + "           " + price * quant + "    \n");
                        flag = true;
                    } else {
                        bill_list_text.setText(bill_list_text.getText() + "                            " + i + "       " + medicine_text.getText() + "          " + quantity_text.getText() + "            " + price + "           " + price * quant + "    \n");
                    }

                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(recipt.class.getName()).log(Level.SEVERE, null, ex);
        }

        quantity -= quant;
        try {
            PreparedStatement stmt = con.prepareStatement("UPDATE `project`.`drugs` SET `Drugs_Quantity` = ? WHERE (`Drugs_ID` = ?)");
            stmt.setInt(1, quantity);
            stmt.setInt(2, i);
            stmt.executeUpdate();
            fillTable();

            //this.setVisible(false);
        } catch (SQLException ex) {
            Logger.getLogger(recipt.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_addbill_buttonActionPerformed

