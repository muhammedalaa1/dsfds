    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String s = search_fld.getText().toString();
        ResultSet rs;
        dtm.setRowCount(0);
        try {
            PreparedStatement stmt = con.prepareStatement("select * from drugs where Drugs_name like '%" + s + "%'");
            rs = stmt.executeQuery();
            while (rs.next()) {
                drugsID.add(rs.getInt(1));
                dtm.addRow(new Object[]{rs.getInt(1), rs.getString(2), rs.getDouble(3), rs.getInt(4), rs.getDate(5), rs.getDate(6), rs.getString(7)});
                medicine_table.setModel(dtm);
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "connection failed");
        }
    }//GEN-LAST:event_jButton1ActionPerformed
